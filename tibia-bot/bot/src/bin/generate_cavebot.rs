//! generate_cavebot — genera un cavebot TOML desde un hunt profile + ciudad.
//!
//! Toma un hunt por `id` (ver assets/catalogs/hunts_10_50.toml), extrae la
//! ciudad asociada de cities.toml, y produce un esqueleto de cavebot con:
//!   - [cavebot] hunt_profile apuntando al profile correcto
//!   - Labels: start / hunt_loop / refill
//!   - Placeholders con comentarios para coords del user calibrar
//!
//! Usage:
//! ```bash
//! cargo run --release --bin generate_cavebot -- --hunt ancient_scarabs_darashia
//! # genera assets/cavebot/ancient_scarabs_darashia.toml
//! ```
//!
//! El user luego:
//! 1. Abre Tibia con NDI + bot arrancado
//! 2. Camina manualmente a cada POI (depot, NPC, spawn)
//! 3. `curl /vision/cursor` para coord de cada click
//! 4. `curl /vision/perception | jq .game_coords` para tile coords de nodes
//! 5. Edita el TOML reemplazando `<PLACEHOLDER_*>` con valores reales
//! 6. Lint: `lint_cavebot` antes de cargar

use clap::Parser;
use std::path::PathBuf;

#[derive(Parser)]
struct Args {
    /// ID del hunt (matcheante con el `id` en hunts_10_50.toml)
    #[arg(long)]
    hunt: String,

    /// Path al catálogo de hunts. Default: assets/catalogs/hunts_10_50.toml
    #[arg(long, default_value = "assets/catalogs/hunts_10_50.toml")]
    hunts_catalog: PathBuf,

    /// Path al catálogo de ciudades. Default: assets/catalogs/cities.toml
    #[arg(long, default_value = "assets/catalogs/cities.toml")]
    cities_catalog: PathBuf,

    /// Directorio output. Default: assets/cavebot/
    #[arg(long, default_value = "assets/cavebot")]
    output_dir: PathBuf,

    /// Sobrescribir si existe (default: error).
    #[arg(long)]
    force: bool,
}

fn main() -> anyhow::Result<()> {
    let args = Args::parse();

    let hunts_raw = std::fs::read_to_string(&args.hunts_catalog)
        .map_err(|e| anyhow::anyhow!("no se pudo leer {}: {}", args.hunts_catalog.display(), e))?;
    let cities_raw = std::fs::read_to_string(&args.cities_catalog)
        .map_err(|e| anyhow::anyhow!("no se pudo leer {}: {}", args.cities_catalog.display(), e))?;

    let hunts: toml::Value = toml::from_str(&hunts_raw)?;
    let cities: toml::Value = toml::from_str(&cities_raw)?;

    // Buscar el hunt por id en el array `[[hunt]]`.
    let hunt_list = hunts.get("hunt")
        .and_then(|v| v.as_array())
        .ok_or_else(|| anyhow::anyhow!("hunts_catalog sin array [[hunt]]"))?;
    let hunt = hunt_list.iter()
        .find(|h| h.get("id").and_then(|v| v.as_str()) == Some(args.hunt.as_str()))
        .ok_or_else(|| anyhow::anyhow!("hunt '{}' no encontrado en el catálogo", args.hunt))?;

    let city_name = hunt.get("city").and_then(|v| v.as_str())
        .ok_or_else(|| anyhow::anyhow!("hunt '{}' sin campo `city`", args.hunt))?;
    let city = cities.get(city_name)
        .ok_or_else(|| anyhow::anyhow!("ciudad '{}' no en cities.toml", city_name))?;

    let depot = city.get("depot_coord").and_then(|v| v.as_array())
        .ok_or_else(|| anyhow::anyhow!("ciudad sin depot_coord"))?;
    let depot_x = depot[0].as_integer().unwrap_or(0);
    let depot_y = depot[1].as_integer().unwrap_or(0);
    let depot_z = depot[2].as_integer().unwrap_or(7);

    let start = hunt.get("start_coord").and_then(|v| v.as_array())
        .ok_or_else(|| anyhow::anyhow!("hunt sin start_coord"))?;
    let start_x = start[0].as_integer().unwrap_or(0);
    let start_y = start[1].as_integer().unwrap_or(0);
    let start_z = start[2].as_integer().unwrap_or(7);

    let hunt_profile_name = hunt.get("id").and_then(|v| v.as_str()).unwrap_or("UNKNOWN");

    let output_path = args.output_dir.join(format!("{}.toml", args.hunt));
    if output_path.exists() && !args.force {
        anyhow::bail!(
            "{} ya existe. Usa --force para sobrescribir.",
            output_path.display()
        );
    }

    let mut out = String::new();
    out.push_str(&format!(
        "# {}.toml — generated by generate_cavebot\n",
        args.hunt
    ));
    out.push_str(&format!("# City: {} (depot {},{},{}), Hunt spawn ({},{},{})\n",
        city_name, depot_x, depot_y, depot_z, start_x, start_y, start_z));
    out.push_str("#\n");
    out.push_str("# TODO: calibrar todos los <PLACEHOLDER_*> con /vision/cursor y GIMP.\n");
    out.push_str("#       Ver README en assets/cavebot/_templates/ para workflow.\n\n");

    out.push_str("[cavebot]\n");
    out.push_str("loop         = true\n");
    out.push_str(&format!("hunt_profile = \"{}\"\n\n", hunt_profile_name));

    out.push_str("# ─── Start / initial checkpoint ─────────────────────────────────\n");
    out.push_str("[[step]]\nkind = \"label\"\nname = \"start\"\n\n");

    out.push_str("# ─── Check supplies (fall to refill if low) ─────────────────────\n");
    out.push_str("[[step]]\nkind         = \"check_supplies\"\non_fail      = \"refill\"\nfrom_profile = true\n\n");

    out.push_str("# ─── Hunt loop entry ────────────────────────────────────────────\n");
    out.push_str("[[step]]\nkind = \"label\"\nname = \"hunt_loop\"\n\n");

    out.push_str(&format!("# Walk al spawn point del hunt ({},{},{})\n", start_x, start_y, start_z));
    out.push_str("# TODO: autogenerar nodes intermedios con path_finder:\n");
    out.push_str(&format!(
        "#   cargo run --release --bin path_finder -- --walkability assets/walkability.bin \\\n\
         #       --from {depot_x},{depot_y},{depot_z} --to {start_x},{start_y},{start_z} --simplify\n"
    ));
    out.push_str(&format!("[[step]]\nkind = \"node\"\nx = {}\ny = {}\nz = {}\n\n", start_x, start_y, start_z));

    out.push_str("# ─── Stand + loot loop ──────────────────────────────────────────\n");
    out.push_str("[[step]]\nkind         = \"stand\"\nuntil        = \"enemies_gte(1)\"\nmax_wait_ms  = 15000\n\n");

    out.push_str("[[step]]\nkind  = \"goto_if\"\nwhen  = \"not:loot_visible\"\nlabel = \"no_loot_skip\"\n\n");

    out.push_str("[[step]]\nkind = \"loot\"\nvx   = 660   # <PLACEHOLDER_LOOT_VX>\nvy   = 400   # <PLACEHOLDER_LOOT_VY>\n\n");

    out.push_str("[[step]]\nkind = \"label\"\nname = \"no_loot_skip\"\n\n");

    out.push_str("# TODO: agregar más nodes del hunt loop (siguiente spawn, etc.)\n\n");

    out.push_str("[[step]]\nkind  = \"goto\"\nlabel = \"start\"\n\n");

    out.push_str("# ═════════════════════════════════════════════════════════════════\n");
    out.push_str("# REFILL ROUTE\n");
    out.push_str("# ═════════════════════════════════════════════════════════════════\n\n");

    out.push_str("[[step]]\nkind = \"label\"\nname = \"refill\"\n\n");

    out.push_str(&format!("# Walk al depot ({},{},{})\n", depot_x, depot_y, depot_z));
    out.push_str(&format!("[[step]]\nkind = \"node\"\nx = {}\ny = {}\nz = {}\n\n", depot_x, depot_y, depot_z));

    out.push_str("# ─── Deposit loot ───────────────────────────────────────────────\n");
    out.push_str("# TODO: calibrar slot_vx/slot_vy con /vision/cursor sobre cada bag.\n");
    out.push_str("# El bloque se repite 1x por cada backpack abierto (ajustar vy +68 por bag).\n");
    out.push_str("[[step]]\n");
    out.push_str("kind         = \"stow_all_items\"\n");
    out.push_str("slot_vx      = 1585   # <PLACEHOLDER_STOW_SLOT_VX>\n");
    out.push_str("slot_vy      = 22     # <PLACEHOLDER_STOW_SLOT_VY>\n");
    out.push_str("menu_offset_x = 87\n");
    out.push_str("menu_offset_y = 258\n");
    out.push_str("menu_wait_ms    = 600\n");
    out.push_str("stow_process_ms = 1200\n");
    out.push_str("max_iterations  = 6\n");
    out.push_str("from_profile    = true\n\n");

    out.push_str("# ─── Walk al NPC shopkeeper ─────────────────────────────────────\n");
    if let Some(npcs) = city.get("npcs").and_then(|v| v.as_array()) {
        for npc in npcs.iter().take(2) {
            if let (Some(name), Some(coord)) = (
                npc.get("name").and_then(|v| v.as_str()),
                npc.get("coord").and_then(|v| v.as_array()),
            ) {
                let nx = coord[0].as_integer().unwrap_or(0);
                let ny = coord[1].as_integer().unwrap_or(0);
                let nz = coord[2].as_integer().unwrap_or(7);
                out.push_str(&format!("# NPC '{}' @ ({},{},{})\n", name, nx, ny, nz));
                out.push_str(&format!("[[step]]\nkind = \"node\"\nx = {}\ny = {}\nz = {}\n\n", nx, ny, nz));
                break;
            }
        }
    }

    out.push_str("# ─── Trade ──────────────────────────────────────────────────────\n");
    out.push_str("# Tibia 12: `trade` command abre la ventana sin click al bag icon.\n");
    out.push_str("# Los bag_button_vx/vy son fallback por si el NPC no acepta `trade`.\n");
    out.push_str("[[step]]\n");
    out.push_str("kind             = \"open_npc_trade\"\n");
    out.push_str("greeting_phrases = [\"hi\", \"trade\"]\n");
    out.push_str("bag_button_vx    = 150   # <PLACEHOLDER_BAG_VX> fallback\n");
    out.push_str("bag_button_vy    = 300   # <PLACEHOLDER_BAG_VY> fallback\n");
    out.push_str("wait_button_ms   = 1500\n\n");

    out.push_str("# Comprar mana potions (filtrar por nombre primero)\n");
    out.push_str("[[step]]\n");
    out.push_str("kind        = \"type_in_field\"\n");
    out.push_str("field_vx    = 180   # <PLACEHOLDER_SEARCH_FIELD_VX>\n");
    out.push_str("field_vy    = 150   # <PLACEHOLDER_SEARCH_FIELD_VY>\n");
    out.push_str("text        = \"mana potion\"\n");
    out.push_str("wait_after_click_ms = 200\n\n");

    out.push_str("[[step]]\n");
    out.push_str("kind        = \"buy_item\"\n");
    out.push_str("item_vx     = 200   # <PLACEHOLDER_ITEM_VX>\n");
    out.push_str("item_vy     = 300   # <PLACEHOLDER_ITEM_VY>\n");
    out.push_str("amount_vx   = 400\n");
    out.push_str("amount_vy   = 500\n");
    out.push_str("confirm_vx  = 480\n");
    out.push_str("confirm_vy  = 540\n");
    out.push_str("quantity    = 100\n");
    out.push_str("spacing_ms  = 200\n\n");

    out.push_str("# ─── Cerrar trade y volver al hunt ──────────────────────────────\n");
    out.push_str("[[step]]\nkind  = \"goto\"\nlabel = \"hunt_loop\"\n");

    std::fs::create_dir_all(&args.output_dir)?;
    std::fs::write(&output_path, out)?;
    println!("generated: {}", output_path.display());
    println!("\n# Next steps:");
    println!("# 1. Abrir {} en editor", output_path.display());
    println!("# 2. Buscar '<PLACEHOLDER_' y reemplazar con coords reales (usar /vision/cursor)");
    println!("# 3. Ejecutar path_finder para nodes entre depot/NPC/spawn si hace falta");
    println!("# 4. Lint: cargo run --release --bin lint_cavebot -- {}", output_path.display());
    println!("# 5. Cargar: POST /cavebot/load?path={}&enabled=false", output_path.display());
    Ok(())
}
